function reducer(state=defaultState,action){
    switch(action.type){
        case actionTypes.CHANGE_SONG_DETAIL:
            return state.set("currentSong",action.currentSong);
        case actionTypes.CHANGE_PLAY_LIST:
            return state.set("playList",action.playList);
        case actionTypes.CHANGE_CURRENT_SONG_INDEX:
            return state.set("currentSongIndex",action.index);
        case actionTypes.CHANGE_SEQUENCE:
            return state.set("sequence",action.sequence);
        case actionTypes.CHANGE_LOCK:
            return state.set("isLock",action.isLock);
        case actionTypes.CHANGE_SHOW_LPAYLIST:
            return state.set("isShowPlayList",action.isShowPlayList);
        case actionTypes.CHANGE_SONG_LYRIC_LIST:
            return state.set("lyricList",action.lyricList)
        default:
            return state;
    }
}

export default reducer;